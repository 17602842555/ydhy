function subsidiaryMetricSql() {
  const metrics = metricDefinitions().filter((metric) => metric.materialized);
  const lines = [];
  for (const item of seed.subsidiaries ?? []) {
    const rowId = sourceRowIds.get(`${item.sourceBatchId}:${item.sourceRow}:${item.id}`);
    const values = {
      monthly_target: item.target,
      monthly_actual: item.actual,
      forecast_rate: item.forecastRate,
      three_day_rate: item.threeDayRate,
      week_rate: item.weekRate,
    };
    for (const metric of metrics) {
      const value = values[metric.code];
      if (value === null || value === undefined) continue;
      lines.push(
        `insert into subsidiary_metrics (id, group_id, subsidiary_id, metric_id, period, value, data_state, import_batch_id, source_row_id) values (${sqlUuid(uuidFor(`subsidiary-metric:${item.id}:${metric.code}:${seed.period}`))}, ${sqlUuid(groupId)}, ${sqlUuid(subsidiaryIds.get(item.id))}, ${sqlUuid(metricIds.get(metric.code))}, ${sql(seed.period)}, ${number(value)}, ${sql(item.dataState)}, ${sqlUuid(batchIds.get(item.sourceBatchId))}, ${sqlUuid(rowId)}) on conflict (id) do update set value = excluded.value, data_state = excluded.data_state;`,
      );
    }
  }
  lines.push('');
  return lines;
}

