function masterDataRecords() {
  const masterData = seed.commercialSystem?.masterData ?? {};
  const buckets = [
    ['legal_entity', masterData.legalEntities ?? []],
    ['brand', masterData.brands ?? []],
    ['channel', masterData.channels ?? []],
    ['product', masterData.products ?? []],
    ['supplier', masterData.suppliers ?? []],
    ['warehouse', masterData.warehouses ?? []],
  ];
  return buckets.flatMap(([type, items]) => items.map((item) => ({ type, item })));
}

function normalizedFileHash(batch) {
  if (/^sha256-[a-f0-9]{12,64}$/.test(batch.hash)) return batch.hash;
  return `sha256-${createHash('sha256').update(`${batch.id}:${batch.fileName}:${batch.hash}`).digest('hex')}`;
}

function uuidFor(input) {
  const bytes = createHash('sha1').update(`huage:${input}`).digest();
  bytes[6] = (bytes[6] & 0x0f) | 0x50;
  bytes[8] = (bytes[8] & 0x3f) | 0x80;
  const hex = bytes.subarray(0, 16).toString('hex');
  return `${hex.slice(0, 8)}-${hex.slice(8, 12)}-${hex.slice(12, 16)}-${hex.slice(16, 20)}-${hex.slice(20, 32)}`;
}

function sql(value) {
  return `'${String(value ?? '').replaceAll("'", "''")}'`;
}

function sqlUuid(value) {
  return `${sql(value)}::uuid`;
}

function sqlJson(value) {
  return `${sql(JSON.stringify(value))}::jsonb`;
}

function sqlTimestamp(value) {
  return `${sql(value)}::timestamptz`;
}

function sqlBool(value) {
  return value ? 'true' : 'false';
}

function int(value) {
  return String(Math.trunc(Number(value) || 0));
}

function number(value) {
  return String(Number(value));
}
