function sourceRowSql() {
  const lines = [];
  for (const item of seed.subsidiaries ?? []) {
    const sourceRowId = uuidFor(`source-row:${item.sourceBatchId}:${item.sourceRow}:${item.id}`);
    sourceRowIds.set(`${item.sourceBatchId}:${item.sourceRow}:${item.id}`, sourceRowId);
    const rawPayload = {
      name: item.name,
      owner: item.owner,
      target: item.target,
      actual: item.actual,
      forecastRate: item.forecastRate,
      threeDayRate: item.threeDayRate,
      weekRate: item.weekRate,
    };
    const normalizedPayload = {
      subsidiaryId: item.id,
      name: item.name,
      target: item.target,
      actual: item.actual,
      forecastRate: item.forecastRate,
      threeDayRate: item.threeDayRate,
      weekRate: item.weekRate,
    };
    lines.push(
      `insert into source_rows (id, batch_id, row_number, raw_payload, normalized_payload, validation_state) values (${sqlUuid(sourceRowId)}, ${sqlUuid(batchIds.get(item.sourceBatchId))}, ${int(item.sourceRow)}, ${sqlJson(rawPayload)}, ${sqlJson(normalizedPayload)}, ${sql(item.dataState === 'raw' ? 'error' : 'valid')}) on conflict (batch_id, row_number) do update set raw_payload = excluded.raw_payload, normalized_payload = excluded.normalized_payload, validation_state = excluded.validation_state;`,
    );
  }
  lines.push('');
  return lines;
}

