function roleSql() {
  const lines = [];
  for (const role of seed.roles ?? []) {
    lines.push(
      `insert into roles (id, name, scope) values (${sql(role.id)}, ${sql(role.name)}, ${sql(role.scope)}) on conflict (id) do update set name = excluded.name, scope = excluded.scope;`,
    );
    for (const permission of role.permissions) {
      lines.push(
        `insert into role_permissions (role_id, permission) values (${sql(role.id)}, ${sql(permission)}) on conflict (role_id, permission) do nothing;`,
      );
    }
  }
  lines.push('');
  return lines;
}

